<template>
  <div>
    <div class="icon-selector" :class="{ 'selector-active': checked }" @click="selectItem">
      <img src="~/assets/img/cart/tick.svg" />
    </div>
  </div>
</template>

<script>
export default {
  name: 'CheckButton',
  props: {
    value: {
      type: Boolean,
      default: true
    }
  },
  data() {
    return {
      checked: this.value
    }
  },
  methods: {
    selectItem() {
      this.$emit('checkBtnClick')
    }
  },
  watch: {
    value(newValue) {
      this.checked = newValue
    }
  }
}
</script>

